# 15. O tipo númerico

# Vendo o tipo de um dado
num = 42
print('Tipo:',type(num))


num2 = 42.0
print('Tipo:',type(num2))


num3 = '42'
print('Tipo:',type(num3))

# Operações com números
# Soma
print(2 + 2)

# Subtração
print(2 - 2)

# Multiplicação
print(2 * 2)

# Divisão
print(2 / 2)

# Potenciação
print(2 ** 2)

# Resultado inteiro da divisão
print(2 // 2)

# Resto da divisão
print(2 % 2)

# Resto da divisão
print(3 % 2)

